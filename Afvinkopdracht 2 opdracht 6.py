standaardrecept = 48
suiker = 1.5
boter = 1
bloem = 2.75

hoeveelheidkoekjes = float(input("Hoeveel wil je er maken?"))

hoeveelheidsuiker = (hoeveelheidkoekjes / standaardrecept) * suiker

hoeveelheidboter = (hoeveelheidkoekjes / standaardrecept) * boter

hoeveelheidbloem = (hoeveelheidkoekjes / standaardrecept) * bloem

print (hoeveelheidkoekjes)+(hoeveelheidsuiker)+(hoeveelheidboter)+(hoeveelheidbloem)

